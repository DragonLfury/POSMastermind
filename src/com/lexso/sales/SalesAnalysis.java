     private void loadInvoiceList(String searchText) {
        try {
            String query = "SELECT invoice.id, invoice.user_email, "
                    + "CONCAT(customer.First_name, ' ', customer.Last_name) AS customer_name, "
                    + "invoice.customer_mobile, SUM(invoice_item.qty) AS total_qty, "
                    + "invoice.paid_amount, invoice.discount, invoice.payment_status, invoice.date "
                    + "FROM invoice_item "
                    + "INNER JOIN invoice ON invoice_item.invoice_id = invoice.id "
                    + "LEFT JOIN customer ON invoice.customer_mobile = customer.mobile ";

            // Add WHERE clause if search text is provided
            if (!searchText.isEmpty()) {
                query += "WHERE invoice.id LIKE '%" + searchText + "%' "
                        + "OR invoice.user_email LIKE '%" + searchText + "%' "
                        + "OR invoice.customer_mobile LIKE '%" + searchText + "%' "
                        + "OR invoice.date LIKE '%" + searchText + "%' "
                        + "OR CONCAT(customer.First_name, ' ', customer.Last_name) LIKE '%" + searchText + "%' "
                        + "OR customer.First_name LIKE '%" + searchText + "%' "
                        + "OR customer.Last_name LIKE '%" + searchText + "%'";
            }

            query += " GROUP BY invoice.id, invoice.user_email, customer_name, invoice.customer_mobile, "
                    + "invoice.paid_amount, invoice.discount, invoice.payment_status, invoice.date";

            ResultSet resultSet = DatabaseConnection.executeSearch(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id")); // Invoice ID
                vector.add(resultSet.getString("user_email")); // Employee Email
                vector.add(resultSet.getString("customer_name")); // Customer Full Name
                vector.add(resultSet.getString("customer_mobile")); // Customer Mobile
                vector.add(resultSet.getString("total_qty")); // Total Items
                vector.add(resultSet.getString("discount")); // Total Discount
                vector.add(resultSet.getString("paid_amount")); // Total Amount
                vector.add(resultSet.getString("payment_status")); // Status
                vector.add(resultSet.getString("date")); // Date & Time
                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading invoice data: " + e.getMessage(),
                    "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }
