    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // invoiceitems open
        // invoiceitems open
        int row = jTable1.getSelectedRow();
        if (row >= 0) { // Check if a valid row is selected
            String invID = String.valueOf(jTable1.getValueAt(row, 0));
            String empEmail = String.valueOf(jTable1.getValueAt(row, 1));
            String cusName = String.valueOf(jTable1.getValueAt(row, 2));
            String cusMobile = String.valueOf(jTable1.getValueAt(row, 3));
            String status = String.valueOf(jTable1.getValueAt(row, 7)); // Payment status is at index 7
            String dateTime = String.valueOf(jTable1.getValueAt(row, 8)); // DateTime is at index 8

            if (evt.getClickCount() == 2) {
                // invoice item dialog open
                System.out.println("invoice items Button Clicked");
                invoiceItems invitemPanel = new invoiceItems();

                // Set the values in the invoiceItems panel
                invitemPanel.setInvoiceID(invID);
                invitemPanel.setEmployeeEmail(empEmail);
                invitemPanel.setCustomerName(cusName);
                invitemPanel.setCustomerMobile(cusMobile);
                invitemPanel.setPaymentStatus(status);
                invitemPanel.setDateTime(dateTime);

                JDialog frame = new JDialog(this, true);
                frame.add(invitemPanel);
                frame.setSize(1105, 710);
                frame.setLocationRelativeTo(null);
                frame.setVisible(true);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

