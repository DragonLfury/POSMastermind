    private void loadToDashboard() {
        try {
            String totalSuppliersQuery = "SELECT COUNT(*) AS total_suppliers FROM `supplier` WHERE `status` = 'active'";
            ResultSet rsSuppliers = DatabaseConnection.executeSearch(totalSuppliersQuery);
            int totalSuppliers = 0;
            if (rsSuppliers.next()) {
                totalSuppliers = rsSuppliers.getInt("total_suppliers");
            }
            totalSuppliersjLabel.setText(String.valueOf(totalSuppliers));

            String productsSuppliedQuery = "SELECT SUM(`order_qty`) AS total_qty_supplied FROM `stock_reorder` WHERE `status` = 'delivered'";
            ResultSet rsProductsSupplied = DatabaseConnection.executeSearch(productsSuppliedQuery);
            int totalQtySupplied = 0;
            if (rsProductsSupplied.next()) {
                totalQtySupplied = rsProductsSupplied.getInt("total_qty_supplied");
            }
            productSuppliedjLabel.setText(String.valueOf(totalQtySupplied));

            LocalDate now = LocalDate.now();
            String firstDayOfMonth = now.withDayOfMonth(1).format(DateTimeFormatter.ofPattern("yyyy-MM-dd"));
            String lastDayOfMonth = now.withDayOfMonth(now.lengthOfMonth()).format(DateTimeFormatter.ofPattern("yyyy-MM-dd"));

            String ordersThisMonthQuery = "SELECT COUNT(*) AS orders_month_count FROM `stock_reorder` WHERE `reorder_date` BETWEEN '" + firstDayOfMonth + "' AND '" + lastDayOfMonth + "'";
            ResultSet rsOrdersMonth = DatabaseConnection.executeSearch(ordersThisMonthQuery);
            int ordersThisMonth = 0;
            if (rsOrdersMonth.next()) {
                ordersThisMonth = rsOrdersMonth.getInt("orders_month_count");
            }
            odersThisMonthjLabel.setText(String.valueOf(ordersThisMonth));

        } catch (Exception e) {
            e.printStackTrace();
            totalSuppliersjLabel.setText("Error");
            productSuppliedjLabel.setText("Error");
            odersThisMonthjLabel.setText("Error");
        }
    }

