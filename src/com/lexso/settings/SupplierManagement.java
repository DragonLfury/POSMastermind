    private void loadCompanyId() {

        try {

            ResultSet resultSet = DatabaseConnection.executeSearch("SELECT * FROM `company`");

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {

                v.add(resultSet.getString("id"));
                company.put(resultSet.getString("name"), resultSet.getString("id"));

            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadSuplier(String mobile) {
        try {

            ResultSet resultSet = DatabaseConnection.executeSearch("SELECT * FROM `supplier`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("company_id"));
                vector.add(resultSet.getString("status"));
                vector.add(resultSet.getString("created_at"));

                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

